package day2;

public class BreakContinue {

	public static void main(String[] args) {
		
      for(int i=1;i<=10;i++)
      {
    	  System.out.println(i +" hello");
    	  if(i>3 & i==7)
    	  {
    		 // break;
    		  continue;
    	  }
    	  System.out.println(i +" java");
      }
      
	}

}
